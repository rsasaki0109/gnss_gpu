def build_initial_pf_smoother_run_result(context: Mapping[str, Any]) -> dict[str, object]:
    result: dict[str, object] = {"run": context["run_name"]}
    for key in _COPY_KEYS:
        result[key] = context[key]

    n_dd_sigma_relaxed = int(context.get("n_dd_sigma_relaxed") or 0)
    result["mean_dd_sigma_scale"] = (
        float(context["dd_sigma_scale_sum"]) / float(n_dd_sigma_relaxed)
        if n_dd_sigma_relaxed > 0
        else None
    )
    result["fgo_local_motion_source"] = context["fgo_motion_source"]
    result.update(
        {
            "forward_metrics": None,
            "smoothed_metrics": None,
            "smoothed_metrics_before_fgo": None,
            "epoch_diagnostics": None,
            "smoother_position_update_sigma": context.get(
                "smoother_position_update_sigma"
            ),
            "n_tail_guard_applied": 0,
            "stop_segment_info": None,
            "n_stop_segment_epochs_applied": 0,
            "stop_segment_static_info": None,
            "n_stop_segment_static_epochs_applied": 0,
            "fgo_local_applied": False,
            "fgo_local_info": None,
            "fgo_local_lambda": bool(context.get("fgo_local_lambda")),
        }
    )
    return result
