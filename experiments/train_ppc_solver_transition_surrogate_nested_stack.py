#!/usr/bin/env python3
"""Strict nested evaluation for binary solver-transition surrogate stacking.

For each outer held-out run:

1. Transition surrogate classifiers are trained without the outer run and
   predict the outer run.
2. Training-run surrogate probabilities are generated by an inner
   leave-one-run-out pass that also excludes the outer run.
3. The residual stacker is trained only on those inner training probabilities
   and predicts the outer run probabilities from step 1.

This avoids the leakage in a simple cross-fit stack where training-run
surrogate probabilities can be produced by models that saw the outer test run.
"""

from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np
import pandas as pd

from train_ppc_solver_transition_surrogate_stack import (
    RESULTS_DIR,
    _add_temporal_prob_features,
    _append_classifier_meta,
    _classifier,
    _groups,
    _key_frame,
    _logit,
    _metric_row,
    _parse_float_list,
    _positive_probability,
    _prediction_rows,
    _read_reference_predictions,
    _residual_model,
    _sim_feature_matrix,
    _target_metric_rows,
    _transition_targets,
    _write_rows,
)


def _fit_probabilities(
    *,
    x_train: np.ndarray,
    y_train: np.ndarray,
    w_train: np.ndarray,
    x_test: np.ndarray,
    random_state: int,
    estimators: int,
    min_leaf: int,
    max_features: float,
) -> np.ndarray:
    clf = _classifier(random_state, estimators, min_leaf, max_features)
    clf.fit(x_train, y_train, sample_weight=w_train)
    return _positive_probability(clf, x_test)


def _nested_transition_probabilities(
    *,
    df: pd.DataFrame,
    x: np.ndarray,
    targets: dict[str, np.ndarray],
    weights: np.ndarray,
    groups: np.ndarray,
    outer_group: int,
    estimators: int,
    min_leaf: int,
    max_features: float,
    random_state: int,
) -> tuple[dict[str, np.ndarray], dict[str, np.ndarray]]:
    """Return probabilities for outer training rows and outer test rows.

    Training probabilities are inner leave-one-run-out predictions over the
    outer training groups only.  Test probabilities are trained on all outer
    training rows.
    """

    train_mask = groups != outer_group
    test_mask = groups == outer_group
    train_groups = sorted(set(int(value) for value in groups[train_mask]))

    train_probs: dict[str, np.ndarray] = {}
    test_probs: dict[str, np.ndarray] = {}
    for target_idx, (target_name, y) in enumerate(targets.items()):
        seed_base = random_state + target_idx * 1000 + outer_group * 100_000
        out_train = np.full(np.count_nonzero(train_mask), np.nan, dtype=np.float64)
        train_indices = np.flatnonzero(train_mask)
        for inner_group in train_groups:
            inner_fit = train_mask & (groups != inner_group)
            inner_pred = train_mask & (groups == inner_group)
            local_positions = np.searchsorted(train_indices, np.flatnonzero(inner_pred))
            out_train[local_positions] = _fit_probabilities(
                x_train=x[inner_fit],
                y_train=y[inner_fit],
                w_train=weights[inner_fit],
                x_test=x[inner_pred],
                random_state=seed_base + inner_group,
                estimators=estimators,
                min_leaf=min_leaf,
                max_features=max_features,
            )
        out_train[~np.isfinite(out_train)] = 0.0
        train_probs[target_name] = out_train
        test_probs[target_name] = _fit_probabilities(
            x_train=x[train_mask],
            y_train=y[train_mask],
            w_train=weights[train_mask],
            x_test=x[test_mask],
            random_state=seed_base + 999,
            estimators=estimators,
            min_leaf=min_leaf,
            max_features=max_features,
        )
    return train_probs, test_probs


def _probability_feature_matrix(df_part: pd.DataFrame, probs: dict[str, np.ndarray]) -> tuple[np.ndarray, list[str]]:
    names = list(probs)
    matrix = np.column_stack([probs[name] for name in names])
    return _add_temporal_prob_features(df_part.reset_index(drop=True), matrix, names)


def _fit_residual_predict(
    *,
    model_name: str,
    x_train: np.ndarray,
    residual_train: np.ndarray,
    w_train: np.ndarray,
    x_test: np.ndarray,
    random_state: int,
    estimators: int,
    min_leaf: int,
    max_features: float,
) -> np.ndarray:
    model = _residual_model(model_name, random_state, estimators, min_leaf, max_features)
    if hasattr(model, "steps"):
        model.fit(x_train, residual_train, model__sample_weight=w_train)
    else:
        model.fit(x_train, residual_train, sample_weight=w_train)
    return np.asarray(model.predict(x_test), dtype=np.float64)


def _nested_residual_predictions(
    *,
    df: pd.DataFrame,
    x_classifier: np.ndarray,
    targets: dict[str, np.ndarray],
    y: np.ndarray,
    base: np.ndarray,
    weights: np.ndarray,
    groups: np.ndarray,
    residual_models: list[str],
    classifier_estimators: int,
    classifier_min_leaf: int,
    classifier_max_features: float,
    residual_estimators: int,
    residual_min_leaf: int,
    residual_max_features: float,
    random_state: int,
) -> tuple[dict[str, np.ndarray], dict[str, np.ndarray]]:
    residual_target = y - base
    nested_probs = {name: np.full(y.shape, np.nan, dtype=np.float64) for name in targets}
    residual_by_model = {name: np.full(y.shape, np.nan, dtype=np.float64) for name in residual_models}

    for outer_group in sorted(set(int(value) for value in groups)):
        train_mask = groups != outer_group
        test_mask = groups == outer_group
        train_probs, test_probs = _nested_transition_probabilities(
            df=df,
            x=x_classifier,
            targets=targets,
            weights=weights,
            groups=groups,
            outer_group=outer_group,
            estimators=classifier_estimators,
            min_leaf=classifier_min_leaf,
            max_features=classifier_max_features,
            random_state=random_state,
        )
        for name, values in test_probs.items():
            nested_probs[name][test_mask] = values

        train_prob_x, _ = _probability_feature_matrix(df.loc[train_mask].copy(), train_probs)
        test_prob_x, _ = _probability_feature_matrix(df.loc[test_mask].copy(), test_probs)
        train_meta = np.column_stack([base[train_mask], _logit(base[train_mask]), train_prob_x])
        test_meta = np.column_stack([base[test_mask], _logit(base[test_mask]), test_prob_x])

        for model_idx, model_name in enumerate(residual_models):
            residual_by_model[model_name][test_mask] = _fit_residual_predict(
                model_name=model_name,
                x_train=train_meta,
                residual_train=residual_target[train_mask],
                w_train=weights[train_mask],
                x_test=test_meta,
                random_state=random_state + 50_000 + outer_group * 100 + model_idx,
                estimators=residual_estimators,
                min_leaf=residual_min_leaf,
                max_features=residual_max_features,
            )

    for name in nested_probs:
        nested_probs[name][~np.isfinite(nested_probs[name])] = 0.0
    for name in residual_by_model:
        residual_by_model[name][~np.isfinite(residual_by_model[name])] = 0.0
    return nested_probs, residual_by_model


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Strict nested binary transition surrogate stack")
    parser.add_argument("--window-csv", type=Path, required=True)
    parser.add_argument("--base-prefix", required=True)
    parser.add_argument("--base-prediction-column", default="corrected_pred_fix_rate_pct")
    parser.add_argument("--classifier-estimators", type=int, default=80)
    parser.add_argument("--classifier-min-leaf", type=int, default=4)
    parser.add_argument("--classifier-max-features", type=float, default=0.75)
    parser.add_argument("--classifier-include-base", action="store_true")
    parser.add_argument("--classifier-include-city", action="store_true")
    parser.add_argument("--classifier-include-run-position", action="store_true")
    parser.add_argument("--include-focused-targets", action="store_true")
    parser.add_argument("--residual-models", default="ridge,extra_trees")
    parser.add_argument("--residual-estimators", type=int, default=80)
    parser.add_argument("--residual-min-leaf", type=int, default=3)
    parser.add_argument("--residual-max-features", type=float, default=0.75)
    parser.add_argument("--alphas", default="0,0.1,0.2,0.3,0.5,0.75,1.0")
    parser.add_argument("--residual-clip-pp", default="5,10,15,20,30,50,100")
    parser.add_argument("--max-run-mae-pp", type=float, default=0.0)
    parser.add_argument("--max-abs-aggregate-error-pp", type=float, default=0.0)
    parser.add_argument("--random-state", type=int, default=2034)
    parser.add_argument("--top-n", type=int, default=50)
    parser.add_argument("--results-prefix", default="ppc_solver_transition_surrogate_nested_stack")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    df = pd.read_csv(args.window_csv).sort_values(["city", "run", "window_index"]).reset_index(drop=True)
    groups, group_labels = _groups(df)
    weights = df["sim_matched_epochs"].to_numpy(dtype=np.float64)
    y = df["actual_fix_rate_pct"].to_numpy(dtype=np.float64) / 100.0
    reference = _read_reference_predictions(args.base_prefix, args.base_prediction_column)
    base = np.asarray([reference[key] for key in _key_frame(df)], dtype=np.float64)
    x, feature_names = _sim_feature_matrix(df)
    x, feature_names = _append_classifier_meta(
        df=df,
        x=x,
        names=feature_names,
        base=base,
        include_base=args.classifier_include_base,
        include_city=args.classifier_include_city,
        include_run_position=args.classifier_include_run_position,
    )
    targets = _transition_targets(df, include_focused_targets=args.include_focused_targets)
    residual_models = [part.strip() for part in args.residual_models.split(",") if part.strip()]
    nested_probs, residual_by_model = _nested_residual_predictions(
        df=df,
        x_classifier=x,
        targets=targets,
        y=y,
        base=base,
        weights=weights,
        groups=groups,
        residual_models=residual_models,
        classifier_estimators=args.classifier_estimators,
        classifier_min_leaf=args.classifier_min_leaf,
        classifier_max_features=args.classifier_max_features,
        residual_estimators=args.residual_estimators,
        residual_min_leaf=args.residual_min_leaf,
        residual_max_features=args.residual_max_features,
        random_state=args.random_state,
    )
    print(f"classifier_features={len(feature_names)} transition_targets={len(targets)} residual_models={len(residual_models)}")

    metric_rows = [
        _metric_row(
            name="base",
            y=y,
            pred=base,
            weights=weights,
            groups=groups,
            group_labels=group_labels,
            params={"residual_model": "base", "alpha": 0.0, "residual_clip_pp": 0.0},
        )
    ]
    corrected_by_name: dict[str, np.ndarray] = {"base": base}
    residual_for_name: dict[str, np.ndarray] = {"base": np.zeros_like(base)}
    alphas = _parse_float_list(args.alphas)
    clip_values = _parse_float_list(args.residual_clip_pp)
    for model_name, residual_pred in residual_by_model.items():
        for alpha in alphas:
            for clip_pp in clip_values:
                clip = clip_pp / 100.0
                pred = np.clip(base + alpha * np.clip(residual_pred, -clip, clip), 0.0, 1.0)
                name = f"{model_name}_alpha{alpha:g}_clip{clip_pp:g}pp"
                corrected_by_name[name] = pred
                residual_for_name[name] = residual_pred
                metric_rows.append(
                    _metric_row(
                        name=name,
                        y=y,
                        pred=pred,
                        weights=weights,
                        groups=groups,
                        group_labels=group_labels,
                        params={
                            "residual_model": model_name,
                            "alpha": alpha,
                            "residual_clip_pp": clip_pp,
                        },
                    )
                )

    metric_rows.sort(key=lambda row: (float(row["window_weighted_mae_pp"]), float(row["run_mae_pp"])))
    candidate_rows = [
        row
        for row in metric_rows
        if (args.max_run_mae_pp <= 0.0 or float(row["run_mae_pp"]) <= args.max_run_mae_pp)
        and (
            args.max_abs_aggregate_error_pp <= 0.0
            or abs(float(row["aggregate_error_pp"])) <= args.max_abs_aggregate_error_pp
        )
    ]
    if not candidate_rows:
        print("selection constraints matched no rows; falling back to unconstrained best")
        candidate_rows = metric_rows
    best = candidate_rows[0]
    best["selection_max_run_mae_pp"] = args.max_run_mae_pp
    best["selection_max_abs_aggregate_error_pp"] = args.max_abs_aggregate_error_pp
    best_pred = corrected_by_name[str(best["model"])]
    best_residual = residual_for_name[str(best["model"])]
    prediction_rows = _prediction_rows(
        df=df,
        y=y,
        base=base,
        residual_pred=best_residual,
        corrected=best_pred,
        probabilities=nested_probs,
    )

    prefix = RESULTS_DIR / args.results_prefix
    _write_rows(prefix.with_name(prefix.name + "_target_metrics.csv"), _target_metric_rows(targets, nested_probs, weights))
    _write_rows(prefix.with_name(prefix.name + "_grid_metrics.csv"), metric_rows)
    _write_rows(prefix.with_name(prefix.name + "_best_model.csv"), [best])
    _write_rows(prefix.with_name(prefix.name + "_window_predictions.csv"), prediction_rows)
    _write_rows(
        prefix.with_name(prefix.name + "_top_gains.csv"),
        sorted(prediction_rows, key=lambda row: -float(row["abs_error_gain_pp"]))[: args.top_n],
    )
    _write_rows(
        prefix.with_name(prefix.name + "_top_regressions.csv"),
        sorted(prediction_rows, key=lambda row: float(row["abs_error_gain_pp"]))[: args.top_n],
    )
    print(
        "best model: "
        f"{best['model']} run_mae={float(best['run_mae_pp']):.3f}pp "
        f"window_weighted_mae={float(best['window_weighted_mae_pp']):.3f}pp "
        f"corr={float(best['corr_window']):.3f}"
    )


if __name__ == "__main__":
    main()
